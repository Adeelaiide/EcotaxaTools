  ggsave(filename= file.path(path, "Biovolume composition per size class.png"),
         width=297, height=210, units = "mm")

    # 8. SHANNON INDEX (DIVERSITY) PER SAMPLE
    # ----------------------------------------------------------------------------
    
  # Plot of Shannon index 
  print (sample.point %>% ggplot(aes(x=factor(sample_num), y=Shannon)) +
         geom_point() + labs(y = "Shannon index [0-5]", x = NULL) +
         theme_bw()+
         theme(axis.text = element_text(size = 10), plot.title = element_text(hjust = 0.5,face = "bold")))
  ggsave(filename= file.path(path, "Shannon Index per sample.png"),
       width=297, height=210, units = "mm")

  #Shannon Index map  
  print(ggplot() +
          geom_sf(data = worldmap, color=NA, fill="gray54") +
          geom_sf(data = sample.point, size=2, aes(color= Shannon)) +
